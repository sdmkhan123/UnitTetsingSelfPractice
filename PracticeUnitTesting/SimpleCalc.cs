using System;
using System.Collections.Generic;
using System.Text;

namespace PracticeUnitTesting
{
    public class SimpleCalc
    {
        public int addFun(int a, int b)
        {
            return a + b;
        }
        public int subFun(int num1, int num2)
        {
            return num1 - num2;
        }
        public int mulFun(int a, int b)
        {
            return a * b;
        }
        public int divFun(int a, int b)
        {
            return a / b;
        }
    }
}
